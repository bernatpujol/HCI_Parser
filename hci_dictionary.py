def decode_opcode(opcode: int):
    """Return (OGF, OCF) from a 16-bit opcode."""
    ogf = (opcode >> 10) & 0x3F
    ocf = opcode & 0x03FF
    return ogf, ocf


def lookup_command(opcode: int) -> str:
    name = HCI_COMMANDS.get(opcode)
    if name:
        return name
    ogf, ocf = decode_opcode(opcode)
    if ogf == 0x3F:
        return f"Vendor-Specific Command (OGF=0x3F, OCF=0x{ocf:03X})"
    return f"Unknown Command (OGF=0x{ogf:02X}, OCF=0x{ocf:03X})"


def lookup_event(event_code: int) -> str:
    return HCI_EVENTS.get(event_code, f"Unknown Event (0x{event_code:02X})")


def lookup_le_subevent(sub: int) -> str:
    return HCI_LE_META_SUBEVENTS.get(sub, f"Unknown LE Sub-event (0x{sub:02X})")